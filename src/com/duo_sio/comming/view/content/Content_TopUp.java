    private void initTable() {
        topUpController.getBalanceTracker().forEach(o ->{
            CashType stat;
            stat = switch (o.getType()) {
                case "in"-> CashType.IN;
                case "out" -> CashType.OUT;
                default -> CashType.OUT;
            };
            table.addRow(new Object[]{CommonUtils.getFormattedPrice(o.getAmount()), o.getCreated_at(), stat});
        });
    }

