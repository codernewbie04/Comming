    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        int saldo;
        try{
            saldo = Integer.parseInt(JOptionPane.showInputDialog("Jumlah topup", "0"));
        } catch(NumberFormatException e){
            saldo = 0;
        }
        if(saldo == 0){
            JOptionPane.showMessageDialog(this, "Gagal TopUp!!", "Failed",JOptionPane.ERROR_MESSAGE);
        } else {
            new ProfileController().tambahSaldo(-1, saldo);
            JOptionPane.showMessageDialog(this, "Berhasil topup TopUp!!", "Success",JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton2MouseClicked


