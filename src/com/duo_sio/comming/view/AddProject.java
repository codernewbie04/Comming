    private void et_startKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_et_startKeyPressed
        String value = et_start.getText();
        int l = value.length();
        if ((evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') || evt.getKeyChar() == '\b') {
           et_start.setEditable(true);
        } else {
            et_start.setEditable(false);
        }
    }//GEN-LAST:event_et_startKeyPressed

