    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        ProfileController profile = new ProfileController();
        if(et_nama.getText().equals("") || et_description.getText().equals("") || et_start.getText().equals("") || et_end.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Form tidak boleh kosong!", "Invalid Form",JOptionPane.ERROR_MESSAGE);
        } else if (Integer.parseInt(et_end.getText()) < Integer.parseInt(et_start.getText())){
            JOptionPane.showMessageDialog(this, "BID Start must be > from BID End", "Invalid Form",JOptionPane.ERROR_MESSAGE);
        } else if(profile.getUser().getSaldo() < Integer.parseInt(et_end.getText())){
            JOptionPane.showMessageDialog(this, "Saldo tidak cukup! Silakan topup terlebih dahulu", "Saldo Kurang!",JOptionPane.ERROR_MESSAGE);
         }else {
            //String nama, String description, int start, int end
            boolean add = projectController.createProject(et_nama.getText(), et_description.getText(), Integer.parseInt(et_start.getText()), Integer.parseInt(et_end.getText()));
            if(add){
                profile.kurangiSaldo(Integer.parseInt(et_end.getText()));
                JOptionPane.showMessageDialog(this, "Berhasil menambah project!", "Success",JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan kedalam database!", "Failed",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2MouseClicked

